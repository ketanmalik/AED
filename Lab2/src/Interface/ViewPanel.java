    private void viewVsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewVsBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblVitalSigns.getSelectedRow();
        if (selectedRow >= 0) {
            VitalSigns vs = (VitalSigns) tblVitalSigns.getValueAt(selectedRow, 0);
            bloodPressureTxtField.setText(String.valueOf(vs.getBloodPressure()));
            temperatureTxtField.setText(String.valueOf(vs.getTemperature()));
            pulseTxtField.setText(String.valueOf(vs.getPulse()));
            dateTxtField.setText(vs.getDate());

        } else {
            JOptionPane.showMessageDialog(null, "Please select a Vital Sign to view");
        }
    }//GEN-LAST:event_viewVsBtnActionPerformed

    private void deleteVsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteVsBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblVitalSigns.getSelectedRow();

        if (selectedRow >= 0) {
            VitalSigns v = (VitalSigns) tblVitalSigns.getValueAt(selectedRow, 0);
            vsh.deleteVitalSigns(v);
            populateTable();
            JOptionPane.showMessageDialog(null, "Vital Sign deleted successfully");

        } else {
            JOptionPane.showMessageDialog(null, "Please select a Vital Sign to delete");
        }
    }//GEN-LAST:event_deleteVsBtnActionPerformed


